    private void btngrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btngrabarActionPerformed

        Integer id = null;
        String nombre = txtNombre.getText();
        String descripcion = txtDescripcion.getText();
        String precioStr = txtPrecio.getText();
        BigDecimal precio = null;
        boolean tieneGarantia = chckbxTieneGarantia.isSelected();
        String duracionGarantia = txtGarantia.getText();
        String mensaje = "";
        
        try {
            precio = new BigDecimal(precioStr);
            if(lblId.getText() != null && !lblId.getText().isEmpty())
            {
             id = Integer.parseInt(lblId.getText());
            }     
            computador = new Computador(id, nombre, descripcion, precio, tieneGarantia,duracionGarantia);
            
            if(computador.getCodigo() != null)
            {
                //UPDATE
                computadoresControl.modificar(computador);
                mensaje = "modificado";
            }else{
                //INSERT
            	computadoresControl.insertar(computador);
                mensaje = "insertado";
            }
            
            this.refresh();

            JOptionPane.showMessageDialog(null, "Se ha "+mensaje+" con exito");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Edad y Peso son numericos y obligatorios");
        }


    }//GEN-LAST:event_btngrabarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
    	List<Computador> computadoresList = new ArrayList<>();
        Integer filaNum = tblComputadores.getSelectedRow();

        if (filaNum<0) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un registro");
        } else {
            DefaultTableModel modelo = (DefaultTableModel) tblComputadores.getModel();

            Integer id = (Integer) modelo.getValueAt(filaNum, 0);
            computadoresList = computadoresControl.listar("WHERE codigo='" + id + "'");
			computador = (Computador) computadoresList.get(0);

            //me aseguro de limpiar antes de cambiar
            limpiar();

            lblId.setText(computador.getCodigo().toString());

            txtNombre.setText(computador.getNombre());
            txtDescripcion.setText(computador.getDescripcion());
            txtPrecio.setText(computador.getPrecio().toString());
            txtGarantia.setText(computador.getDuracionGarantia());
            chckbxTieneGarantia.setSelected(computador.isTieneGarantia());

            btngrabar.setText("Modificar");
        }


    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
    	List<Computador> computadoresList = new ArrayList<>();
    	Integer filaNum = tblComputadores.getSelectedRow();

        if (filaNum<0) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un registro");
        } else {
            int input = JOptionPane.showConfirmDialog(null, "Desea Eliminar el Objeto?");
            
            

            switch (input) {
                case SI:
                    DefaultTableModel modelo = (DefaultTableModel) tblComputadores.getModel();
                    Integer id = (Integer) modelo.getValueAt(filaNum, 0);
                    computadoresList = computadoresControl.listar("WHERE codigo='" + id + "'");
                    computador = (Computador) computadoresList.get(0);
                    
                    computadoresControl.eliminar(computador);
                    break;
                case NO:
                    JOptionPane.showMessageDialog(null, "Sabia decision");
                    break;
                case CANCELAR:
                   JOptionPane.showMessageDialog(null, "Evento Cancelado");
                    break;
            }
            this.refresh();
        }
        
    }//GEN-LAST:event_btnEliminarActionPerformed

