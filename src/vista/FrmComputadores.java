    public void refresh() {
    	List<Computador> computadoresList = new ArrayList<>();
    	DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Codigo");
        modelo.addColumn("Nombre");
        modelo.addColumn("Descripcion");
        modelo.addColumn("Precio");
        modelo.addColumn("Garantia");
        modelo.addColumn("Duracion");
        
        computadoresList = computadoresControl.listar("");
        Object[] fila = new Object[6];

        for (int i = 0; i < computadoresList.size(); i++) {
            computador = (Computador) computadoresList.get(i);
            fila[0] = computador.getCodigo();
            fila[1] = computador.getNombre();
            fila[2] = computador.getDescripcion();
            fila[3] = computador.getPrecio();
            fila[4] = computador.isTieneGarantia() ? "SI": "NO";
            fila[5] = computador.getDuracionGarantia();
            modelo.addRow(fila);
        }

        tblComputadores.setModel(modelo);

        limpiar();

    }

